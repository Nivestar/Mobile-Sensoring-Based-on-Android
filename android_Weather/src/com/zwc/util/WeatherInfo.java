package com.zwc.util;

public class WeatherInfo {

	private String dayWeather = null;
	private String nightWeather = null;
	private String dayTemp = null;
	private String nightTemp = null;
	private String dayWindDirection = null;
	private String nightWindDirection = null;
	private String dayWindDegree = null;
	private String nightWindDegree = null;
	private String sun = null;

	public String getDayWeather() {
		return dayWeather;
	}

	public void setDayWeather(String dayWeather) {
		this.dayWeather = dayWeather;
	}

	public String getNightWeather() {
		return nightWeather;
	}

	public void setNightWeather(String nightWeather) {
		this.nightWeather = nightWeather;
	}

	public String getDayTemp() {
		return dayTemp;
	}

	public void setDayTemp(String dayTemp) {
		this.dayTemp = dayTemp;
	}

	public String getNightTemp() {
		return nightTemp;
	}

	public void setNightTemp(String nightTemp) {
		this.nightTemp = nightTemp;
	}

	public String getDayWindDirection() {
		return dayWindDirection;
	}

	public void setDayWindDirection(String dayWindDirection) {
		this.dayWindDirection = dayWindDirection;
	}

	public String getNightWindDirection() {
		return nightWindDirection;
	}

	public void setNightWindDirection(String nightWindDirection) {
		this.nightWindDirection = nightWindDirection;
	}

	public String getDayWindDegree() {
		return dayWindDegree;
	}

	public void setDayWindDegree(String dayWindDegree) {
		this.dayWindDegree = dayWindDegree;
	}

	public String getNightWindDegree() {
		return nightWindDegree;
	}

	public void setNightWindDegree(String nightWindDegree) {
		this.nightWindDegree = nightWindDegree;
	}

	public String getSun() {
		return sun;
	}

	public void setSun(String sun) {
		this.sun = sun;
	}

}
